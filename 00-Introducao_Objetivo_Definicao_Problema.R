## Definição do Problema de Negócio

# - A análise de dados socioeconômicos é fundamental para qualquer empresa compreender o cenário social e 
#   econômico para o ambiente de negócios, compreender as diferenças entre regiões, cidades e países, a
#   influência de indicadores nas decisões de compra dos clientes e como as mudanças no cenário podem impactar
#   as estratégias corporativas.

# - Neste mini-projeto são aplicadas técnicas de análise exploratória em dados socioeconômicos reais disponíveis
#   publicamente. Por se tratar de dados reais, apresentam diversos problemas que são então tratados no processo
#   de limpeza antes da análise.

# - Para a análise os dados são reorganizados usando uma interessante estratégia de mudança de perspectiva dos
#   dados. Então, 5 perguntas de negócio são respondidas através de gráficos e estatísticas.

